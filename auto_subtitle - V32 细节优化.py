def phase_1_transcribe(file_path, whisper_model, mode):
    base_name = file_path.with_suffix('')
    srt_path = Path(f"{base_name}.srt")
    ass_path = Path(f"{base_name}.ass")
    lang_path = file_path.with_suffix('.lang')

    if mode == '1' and srt_path.exists() and ass_path.exists():
        print(f"⏭️ 目标双语字幕已存在，跳过听写: {file_path.name}")
        return True
    elif mode == '2' and srt_path.exists():
        print(f"⏭️ SRT 存在，跳过听写: {file_path.name}")
        return True

    audio_target, is_temp = extract_and_sanitize_audio(file_path)
    try:
        raw_subtitles, language = transcribe_audio(audio_target, whisper_model)
    finally:
        if is_temp and audio_target.exists():
            try: audio_target.unlink()
            except: pass

    if not raw_subtitles: return False
        
    cleaned_subtitles, hall_count = clean_subtitles(raw_subtitles)
    print(f"🧹 基础清洗完毕: 拦截 {hall_count} 处幻觉。")

    save_srt_file(cleaned_subtitles, srt_path)

    with open(lang_path, 'w', encoding='utf-8') as f:
        f.write(language)

    is_chinese = language.lower().startswith('zh')
    if is_chinese and mode == '1':
        save_ass_file(cleaned_subtitles, ass_path, is_chinese_only=True)
        print("✅ 检测为中文，已直出 ASS。")
        if lang_path.exists():
            try: lang_path.unlink()
            except: pass
        
    return True

def run_whisper_process(media_files_str, mode):
    try:
        from faster_whisper import WhisperModel
        print("⏳ [独立子进程] 正在挂载 Whisper 模型至显存...")
        whisper_model = WhisperModel(WHISPER_MODEL_SIZE, device=WHISPER_DEVICE, compute_type=WHISPER_COMPUTE_TYPE, download_root=WHISPER_DOWNLOAD_ROOT, cpu_threads=WHISPER_CPU_THREADS, local_files_only=WHISPER_LOCAL_ONLY)
        
        media_files = [Path(p) for p in media_files_str]
        for file_path in media_files:
            phase_1_transcribe(file_path, whisper_model, mode)
            
        print("\n🗑️ [内存管理] 阶段 1 任务完成。即刻自我销毁以交还操作系统显存...")
    except Exception as e:
        print(f"\n❌ [子进程致命错误] {e}")
        traceback.print_exc()
    finally:
        os._exit(0)

def phase_2_llm_process(file_path, mode):
    base_name = file_path.with_suffix('')
    srt_path = file_path if mode == '3' else Path(f"{base_name}.srt")
    ass_path = Path(f"{base_name}.ass")
    lang_path = file_path.with_suffix('.lang')

    print(f"\n📄 正在解析字幕准备大模型处理: {srt_path.name}")
    cleaned_subtitles = parse_srt_file(srt_path)
    if not cleaned_subtitles: 
        print(f"⚠️ 读取的 SRT 格式不合法或内容为空，跳过处理。")
        return
        
    cleaned_lines = len(cleaned_subtitles)

    lang = 'auto'
    if lang_path.exists():
        with open(lang_path, 'r', encoding='utf-8') as f:
            lang = f.read().strip()

    if lang.startswith('zh') or lang == 'ja':
        print(f"\n🛠️ 启动 Agent 1 (文本校对): 使用 {MODEL_NAME}，当前语言策略: {lang}")
        for i in range(0, cleaned_lines, BATCH_SIZE):
            batch = cleaned_subtitles[i : i + BATCH_SIZE]
            context = cleaned_subtitles[max(0, i - CONTEXT_SIZE) : i]
            print(f"⏳ 正在校对批次: {i+1} - {min(i + BATCH_SIZE, cleaned_lines)} / {cleaned_lines} 行")
            ai_clean_batch(batch, context, lang)
    else:
        print(f"\n⏭️ 检测为 {lang} 语种，跳过 Agent 1 (文本校对)，保留 Whisper 原声高精度字幕...")
        for sub in cleaned_subtitles:
            sub['clean_text'] = sub['text']

    save_srt_file(cleaned_subtitles, srt_path)

    trans_prompt = get_translator_prompt(lang)
    if trans_prompt is None:
        print(f"\n⏭️ 检测为中文体系 ({lang})，跳过 Agent 2 翻译环节，直接生成单语 ASS 字幕...")
        save_ass_file(cleaned_subtitles, ass_path, is_chinese_only=True)
        
        if lang_path.exists():
            try: lang_path.unlink()
            except: pass
            
        print(f"✅ {file_path.name} 单体流水线执行完毕！")
        return

    print(f"\n🚀 启动 Agent 2 (跨语言翻译): 继续使用 {MODEL_NAME}")
    for i in range(0, cleaned_lines, BATCH_SIZE):
        batch = cleaned_subtitles[i : i + BATCH_SIZE]
        context = cleaned_subtitles[max(0, i - CONTEXT_SIZE) : i]
        print(f"⏳ 正在翻译批次: {i+1} - {min(i + BATCH_SIZE, cleaned_lines)} / {cleaned_lines} 行")
        translate_batch(batch, context, lang)
        save_ass_file(cleaned_subtitles, ass_path, is_chinese_only=False)

    if lang_path.exists():
        try: lang_path.unlink()
        except: pass
        
    print(f"✅ {file_path.name} 单体流水线执行完毕！")

def run_pipeline():
    print("╔══════════════════════════════════════════╗")
    print("║ 大模型单体双 Agent 字幕流水线             ║")
    print("╚══════════════════════════════════════════╝")
    mode = input("👉 请输入模式编号 (1/听译全流 2/仅听写 3/仅翻译): ").strip() or '1'
    
    # 支持拖入文件或文件夹
    target_input = input("📁 请拖入目标文件或文件夹路径: ").strip().strip('"').strip("'")
    target_path = Path(target_input)

    if not target_path.exists(): 
        print("❌ 路径不存在！")
        return

    media_files = []
    if target_path.is_file():
        if (mode in ['1', '2'] and target_path.suffix.lower() in SUPPORTED_EXTENSIONS) or \
           (mode == '3' and target_path.suffix.lower() == '.srt'):
            media_files.append(target_path)
        else:
            print(f"❌ 不支持的文件格式或当前模式不匹配: {target_path.name}")
            return
    elif target_path.is_dir():
        recursive = input("🗂️ 扫描子目录？(y/N): ").strip().lower() == 'y'
        media_files = [f for f in (target_path.rglob('*') if recursive else target_path.glob('*')) 
                       if f.is_file() and ((mode in ['1','2'] and f.suffix.lower() in SUPPORTED_EXTENSIONS) or 
                                           (mode == '3' and f.suffix.lower() == '.srt'))]

    if not media_files: 
        print("❌ 在该路径下没有找到可处理的文件。")
        return
        
    print(f"🎯 共发现 {len(media_files)} 个初始任务。")
    
    # 记录总任务开始时间
    pipeline_start_time = time.time()

    # ================= 阶段 1 (物理隔离) =================
    if mode in ['1', '2']:
        print("\n" + "="*50)
        print("▶️ [阶段 1] 开始全局批量语音识别 (启动物理隔离子进程)...")
        print("="*50)
        
        media_files_str = [str(f) for f in media_files]
        p = multiprocessing.Process(target=run_whisper_process, args=(media_files_str, mode))
        p.start()
        p.join() 

        print("✅ [系统回收] Whisper 子进程已物理销毁，Windows 操作系统已 100% 回收显存。")
    
    # ================= 阶段 2 =================
    if mode in ['1', '3']:
        files_for_llm = []
        if mode == '1':
            for file_path in media_files:
                base_name = file_path.with_suffix('')
                srt_path = Path(f"{base_name}.srt")
                ass_path = Path(f"{base_name}.ass")
                if srt_path.exists() and not ass_path.exists():
                    files_for_llm.append(file_path)
        elif mode == '3':
            files_for_llm = media_files

        if files_for_llm:
            print("\n" + "="*50)
            print(f"▶️ [阶段 2] 开始全局大模型文本校对与翻译 (待处理文件: {len(files_for_llm)}个)")
            print("="*50)
            
            if not check_llm_ready():
                return

            for file_path in files_for_llm:
                phase_2_llm_process(file_path, mode)
                
            unload_llm(MODEL_NAME)
        else:
            print("\n⚠️ 阶段二被跳过：所有文件皆已被识别为中文或历史 ASS 已存在。")

    # 结算总耗时
    total_time = time.time() - pipeline_start_time
    hours, rem = divmod(total_time, 3600)
    minutes, seconds = divmod(rem, 60)
    
    print("\n" + "🎉" * 20)
    print(f"✅ 所有任务流水线已全部完成！")
    print(f"⏱️ 任务总耗时: {int(hours)}小时 {int(minutes)}分钟 {seconds:.2f}秒")
    print("🎉" * 20)

