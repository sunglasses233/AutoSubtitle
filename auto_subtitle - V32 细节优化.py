def format_time_srt(seconds):
    hours = math.floor(seconds / 3600)
    seconds %= 3600
    minutes = math.floor(seconds / 60)
    seconds %= 60
    milliseconds = round((seconds - math.floor(seconds)) * 1000)
    return f"{hours:02d}:{minutes:02d}:{math.floor(seconds):02d},{milliseconds:03d}"

def srt_time_to_ass(time_str):
    start_str, end_str = time_str.split('-->')
    def convert(t):
        parts = t.strip().replace(',', ':').split(':')
        return f"{int(parts[0])}:{parts[1]}:{parts[2]}.{parts[3][:2]}"
    return convert(start_str), convert(end_str)

def clean_subtitles(subtitles):
    if not subtitles: return [], 0
    cleaned, hallucination_count = [], 0
    WINDOW_SIZE, MAX_OCCURRENCE = 10, 2 
    for sub in subtitles:
        current_text = sub['text'].strip()
        recent_texts = [s['text'].strip() for s in cleaned[-WINDOW_SIZE:]]
        if recent_texts.count(current_text) >= MAX_OCCURRENCE:
            hallucination_count += 1
        else:
            cleaned.append(sub)
    for i, sub in enumerate(cleaned): sub['id'] = str(i + 1)
    return cleaned, hallucination_count

def parse_srt_file(srt_path):
    subtitles = []
    try:
        with open(srt_path, 'r', encoding='utf-8') as f: content = f.read().strip()
    except Exception as e:
        print(f"❌ 无法读取字幕文件 {srt_path.name}: {e}")
        return subtitles
    blocks = re.split(r'\n\n+', content)
    for block in blocks:
        lines = block.strip().split('\n')
        if len(lines) >= 3:
            subtitles.append({
                'id': lines[0].strip(),
                'time': lines[1].strip(),
                'text': " ".join([line.strip() for line in lines[2:]]),
                'clean_text': " ".join([line.strip() for line in lines[2:]]),
                'translated': ''
            })
    return subtitles

def save_srt_file(subtitles, srt_path):
    with open(srt_path, 'w', encoding='utf-8') as f:
        for sub in subtitles:
            out_text = sub.get('clean_text') or sub['text']
            f.write(f"{sub['id']}\n{sub['time']}\n{out_text}\n\n")

