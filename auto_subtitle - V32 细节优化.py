def ai_clean_batch(batch_subs, context_subs, lang):
    context_text = " ".join([s['clean_text'] or s['text'] for s in context_subs])
    target_ids = [sub['id'] for sub in batch_subs]
    target_text = "\n".join([f"[ID: {sub['id']}] {sub['text']}" for sub in batch_subs])

    user_content = ""
    if context_text:
        user_content += f"【前文语境参考】:\n{context_text}\n\n"
    user_content += f"【待校对的字幕】:\n{target_text}"

    best_cleaned_dict = {}
    
    for attempt in range(MAX_RETRIES):
        try:
            response = CLIENT.chat.completions.create(
                model=MODEL_NAME,
                messages=[
                    {"role": "system", "content": get_cleaner_prompt(lang)},
                    {"role": "user", "content": user_content}
                ],
                temperature=0.3, top_p=0.8, max_tokens=2500,
                extra_body={"chat_template_kwargs": {"enable_thinking": False}}
            )
            
            result_text = response.choices[0].message.content.strip()
            result_text = re.sub(r'<think>.*?</think>', '', result_text, flags=re.DOTALL | re.IGNORECASE).strip()
            
            pattern = r'\[ID:\s*(\d+)\](.*?)(?=\[ID:|\Z)'
            matches = list(re.finditer(pattern, result_text, re.IGNORECASE | re.DOTALL))
            
            for match in matches:
                pid = match.group(1)
                c_text = match.group(2).strip()
                if c_text and pid in target_ids:
                    best_cleaned_dict[pid] = c_text

            if len(best_cleaned_dict) >= len(batch_subs) * 0.8:
                break
                
        except Exception as e:
            print(f"\n⚠️ [警告] 清洗 API 调用失败，正在重试 ({attempt+1}/{MAX_RETRIES})... 异常: {e}")
            time.sleep(3) 
    else:
        print("\n❌ 连续多次调用失败，跳过当前批次清洗！")

    print(f"\n--- 📝 文本清洗对比 (Agent 1 - {lang}) ---")
    for sub in batch_subs:
        if sub['id'] in best_cleaned_dict:
            sub['clean_text'] = best_cleaned_dict[sub['id']]
        else:
            sub['clean_text'] = sub['text'] 
            
        orig = sub['text']
        cleaned = sub['clean_text']
        if orig != cleaned:
            print(f"[ID: {sub['id']}] 🔧 修正: {orig}\n          => {cleaned}")
        else:
            print(f"[ID: {sub['id']}] ✨ 保持: {orig}")
    print("---------------------------------\n")

def translate_batch(batch_subs, context_subs, lang):
    context_text = " ".join([s['translated'] for s in context_subs if s['translated']])
    target_ids = [sub['id'] for sub in batch_subs]
    target_text = "\n".join([f"[ID: {sub['id']}] {sub['clean_text']}" for sub in batch_subs])

    user_content = ""
    if context_text:
        user_content += f"【前文语境参考】:\n{context_text}\n\n"
    user_content += f"【待翻译的字幕】:\n{target_text}"

    best_translated_dict = {}

    for attempt in range(MAX_RETRIES):
        try:
            response = CLIENT.chat.completions.create(
                model=MODEL_NAME,
                messages=[
                    {"role": "system", "content": get_translator_prompt(lang)},
                    {"role": "user", "content": user_content}
                ],
                temperature=0.7, top_p=0.8, max_tokens=2500,
                extra_body={"chat_template_kwargs": {"enable_thinking": False}}
            )
            
            result_text = response.choices[0].message.content.strip()
            result_text = re.sub(r'<think>.*?</think>', '', result_text, flags=re.DOTALL | re.IGNORECASE).strip()
            
            pattern = r'\[ID:\s*(\d+)\](.*?)(?=\[ID:|\Z)'
            matches = list(re.finditer(pattern, result_text, re.IGNORECASE | re.DOTALL))
            
            for match in matches:
                pid = match.group(1)
                trans_text = match.group(2).strip()
                if trans_text and pid in target_ids:
                    best_translated_dict[pid] = trans_text

            if len(best_translated_dict) >= len(batch_subs) * 0.8:
                break
                
        except Exception as e:
            print(f"\n⚠️ [警告] 翻译 API 调用失败，正在重试 ({attempt+1}/{MAX_RETRIES})... 异常: {e}")
            time.sleep(3) 
    else:
        print("\n❌ 连续多次调用失败，跳过当前批次翻译！")

    missing_ids = [sub['id'] for sub in batch_subs if sub['id'] not in best_translated_dict]
    if missing_ids:
        base_context_list = [s['translated'] for s in context_subs if s['translated']]
        for missing_id in missing_ids:
            current_index = next((i for i, sub in enumerate(batch_subs) if sub['id'] == missing_id), -1)
            if current_index == -1: continue
            orig_text = batch_subs[current_index]['clean_text']
            
            current_batch_context = [best_translated_dict[batch_subs[i]['id']] for i in range(current_index) if batch_subs[i]['id'] in best_translated_dict]
            full_context_list = base_context_list + current_batch_context
            dynamic_context_str = " ".join(full_context_list[-CONTEXT_SIZE:])
            
            sys_prompt = "你是一个专业的字幕翻译。请把待翻译的源语言文本直接翻译成中文。只输出中文，严禁解释，严禁使用<think>标签。"
            rescue_prompt = f"【前文语境】:\n{dynamic_context_str}\n\n【待翻译文本】:\n{orig_text}"
            
            try:
                resp = CLIENT.chat.completions.create(
                    model=MODEL_NAME,
                    messages=[{"role": "system", "content": sys_prompt}, {"role": "user", "content": rescue_prompt}],
                    temperature=0.2, max_tokens=300 
                )
                rescue_text = resp.choices[0].message.content.strip()
                rescue_text = re.sub(r'<think>.*?</think>', '', rescue_text, flags=re.DOTALL | re.IGNORECASE).strip()
                rescue_text = re.sub(r'^(好的|翻译|中文|如下|翻译结果)[:：，。\s]*', '', rescue_text, flags=re.IGNORECASE).strip()
                if rescue_text: best_translated_dict[missing_id] = rescue_text
            except: pass

    print(f"\n--- 翻译进度流 (Agent 2 - {lang}) ---")
    for sub in batch_subs:
        trans_text = best_translated_dict.get(sub['id'])
        if trans_text:
            sub['translated'] = trans_text
            print(f"[ID: {sub['id']}] {trans_text}")
        else:
            sub['translated'] = sub['clean_text']
            print(f"[ID: {sub['id']}] (⚠️保留原文) {sub['clean_text']}")
    print("----------------------------\n")

