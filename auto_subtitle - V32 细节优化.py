def transcribe_audio(file_path, model):
    print(f"\n🎙️ 正在转录: {file_path.name}")
    
    segments_generator, info = model.transcribe(
        str(file_path), 
        beam_size=5,
        vad_filter=True,                   
        vad_parameters=dict(min_silence_duration_ms=500), 
        condition_on_previous_text=False,
        word_timestamps=True  
    )
    
    lang_code = info.language.lower()
    print(f"🌐 检测到语言: {lang_code} (置信度: {info.language_probability:.2f})")
    print("-" * 50) 
    
    subtitles = []
    seg_id = 1
    
    # ================= 🚀 双引擎路由逻辑 (恢复实时流式输出) =================
    if lang_code.startswith('zh') or lang_code == 'ja':
        print("⏳ 正在提取原声字幕 (路由策略: CJK 原生段落引擎)...") 
        for segment in segments_generator:
            start_time = format_time_srt(segment.start)
            end_time = format_time_srt(segment.end)
            text = segment.text.strip()
            print(f"[{start_time} -> {end_time}] {text}")
            
            subtitles.append({
                'id': str(seg_id),
                'time': f"{start_time} --> {end_time}",
                'text': text,
                'clean_text': '', 
                'translated': ''
            })  
            seg_id += 1
            
    else:
        print("⏳ 正在提取原声字幕 (路由策略: 英语/表音文字 段落级前瞻容忍引擎)...") 
        for segment in segments_generator:
            if segment.words:
                current_words = []
                chunk_start = segment.words[0].start
                
                for i, word_obj in enumerate(segment.words):
                    current_words.append(word_obj.word)
                    
                    is_last_word = (i == len(segment.words) - 1)
                    words_left = len(segment.words) - 1 - i  
                    
                    gap_to_next = 0.0
                    if not is_last_word:
                        gap_to_next = segment.words[i+1].start - word_obj.end
                        
                    current_text = "".join(current_words).strip()
                    must_cut = False
                    
                    if is_last_word:
                        must_cut = True
                    elif len(current_text) >= EN_ABSOLUTE_MAX_CHARS:
                        must_cut = True
                    elif len(current_text) >= EN_MAX_CHARS or gap_to_next >= EN_MAX_GAP:
                        if words_left <= EN_TOLERANCE_WORDS:
                            must_cut = False 
                        else:
                            must_cut = True
                    
                    if must_cut:
                        chunk_end = word_obj.end
                        start_time = format_time_srt(chunk_start)
                        end_time = format_time_srt(chunk_end)
                        
                        print(f"[{start_time} -> {end_time}] {current_text}")
                        
                        subtitles.append({
                            'id': str(seg_id),
                            'time': f"{start_time} --> {end_time}",
                            'text': current_text,
                            'clean_text': '', 
                            'translated': ''
                        })
                        seg_id += 1
                        
                        if not is_last_word:
                            chunk_start = segment.words[i+1].start
                        current_words = []
            else:
                start_time = format_time_srt(segment.start)
                end_time = format_time_srt(segment.end)
                text = segment.text.strip()
                print(f"[{start_time} -> {end_time}] {text}")
                
                subtitles.append({
                    'id': str(seg_id),
                    'time': f"{start_time} --> {end_time}",
                    'text': text,
                    'clean_text': '', 
                    'translated': ''
                })  
                seg_id += 1

    return subtitles, lang_code

